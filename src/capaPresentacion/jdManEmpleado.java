    public void listarEmpleados() {
        ResultSet rsEmpleados = null;
        String sexo = "";
        String vigencia = "";
        DefaultTableModel modeloEmp = new DefaultTableModel() {
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        modeloEmp.addColumn("DNI");
        modeloEmp.addColumn("Nombre");
        modeloEmp.addColumn("Apellidos");
        modeloEmp.addColumn("Direccion");
        modeloEmp.addColumn("Telefono");
        modeloEmp.addColumn("Correo");
        modeloEmp.addColumn("Fecha Nac");
        modeloEmp.addColumn("Sexo");
        modeloEmp.addColumn("Vigemcia");

        tblEmpleado.setModel(modeloEmp);

        try {
            rsEmpleados = objEmpleado.listarEmpleado();
            while (rsEmpleados.next()) {
                sexo = (rsEmpleados.getBoolean("sexo")) ? "M" : "F";
                vigencia = (rsEmpleados.getBoolean("vigencia")) ? "Vigente" : "No Vigente";
                modeloEmp.addRow(new Object[]{
                    rsEmpleados.getString("dniempleado"),
                    rsEmpleados.getString("nombre"),
                    rsEmpleados.getString("apellidos"),
                    rsEmpleados.getString("direccion"),
                    rsEmpleados.getString("telefono"),
                    rsEmpleados.getString("correo"),
                    rsEmpleados.getDate("fechanac"),
                    sexo,
                    vigencia
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }
