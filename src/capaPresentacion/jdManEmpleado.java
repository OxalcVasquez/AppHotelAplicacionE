    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        ResultSet rsEmpleado = null;
        String sexo = "";
        try {
            if (txtDni.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ingrese DNI a buscar");
                txtDni.requestFocus();
            } else {
                rsEmpleado = objEmpleado.buscarEmpleado(txtDni.getText());
                if (rsEmpleado.next()) {
                    sexo = (rsEmpleado.getBoolean("sexo")) ? "M" : "F";
                    txtNombre.setText(rsEmpleado.getString("nombre"));
                    txtApellidos.setText(rsEmpleado.getString("apellidos"));
                    txtDireccion.setText(rsEmpleado.getString("direccion"));
                    txtCorreo.setText(rsEmpleado.getString("correo"));
                    txtTelefono.setText(rsEmpleado.getString("telefono"));
                    cboSexo.setSelectedItem(sexo);
                    jdFechaNacimiento.setDate(rsEmpleado.getDate("fechanac"));
                    chkVigencia.setSelected(rsEmpleado.getBoolean("vigencia"));

                } else {
                    JOptionPane.showMessageDialog(this, "DNI no registrado");
                    limpiarControles();
                }

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

