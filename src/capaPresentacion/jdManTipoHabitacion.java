    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        ResultSet rsTH = null;
        try {
            if (txtCodigo.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ingrese c칩digo a buscar");
                txtCodigo.requestFocus();
            } else {
                rsTH = objTH.buscarTH(Integer.parseInt(txtCodigo.getText()));
                if (rsTH.next()) {
                    txtNombre.setText(rsTH.getString("nombre"));
                    txtDescripcion.setText(rsTH.getString("descripcion"));
                    txtCosto.setText(String.valueOf(rsTH.getDouble("costo")));
                    chkVigencia.setSelected(rsTH.getBoolean("vigencia"));

                } else {
                    JOptionPane.showMessageDialog(this, "C칩digo no registrado");
                    limpiarControles();
                }

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        try {
            if (!txtCodigo.getText().isEmpty()) {
                if (objTH.validarParaEliminar(Integer.parseInt(txtCodigo.getText()))) {
                    objTH.eliminarTH_Transaccion(Integer.parseInt(txtCodigo.getText()));
                    limpiarControles();
                    listarTipoHabitaciones();
                } else {
                    JOptionPane.showMessageDialog(this, "No se ha podido eliminar dibido que existe hospedajes con la habitacion");

                }

            } else {
                JOptionPane.showMessageDialog(this, "Ingrese c칩digo a eliminar");

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        try {
            objTH.modificarTH(Integer.parseInt(txtCodigo.getText()),
                    txtNombre.getText(), txtDescripcion.getText(),
                    Double.parseDouble(txtCosto.getText()));
            if (chkVigencia.isSelected()) {
                objTH.darAltaTH_Transaccion(Integer.parseInt(txtCodigo.getText()));

            } else {
                objTH.darBajaTH_Transaccion(Integer.parseInt(txtCodigo.getText()));

            }
            limpiarControles();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }


    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
        try {
            if (btnNuevo.getText().equals("Nuevo")) {
                limpiarControles();
                btnNuevo.setText("Guardar");
                txtCodigo.setText(objTH.generarCodigoTH().toString());
                txtNombre.requestFocus();
            } else {
                btnNuevo.setText("Nuevo");
                //Guardar 
                objTH.registrarTH(Integer.parseInt(txtCodigo.getText()), txtNombre.getText(), txtDescripcion.getText(), chkVigencia.isSelected(), Double.parseDouble(txtCosto.getText()));
                limpiarControles();

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnNuevoActionPerformed
