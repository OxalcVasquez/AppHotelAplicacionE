    private void listarTipoHabitaciones() {
        ResultSet rsListaTH = null;
        String vigencia = "";
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("C칩digo");
        modelo.addColumn("Nombre");
        modelo.addColumn("Descripcion");
        modelo.addColumn("Costo");
        modelo.addColumn("Vigencia");
        tblTipoH.setModel(modelo);

        try {
            rsListaTH = objTH.listarTipoHab();
            while (rsListaTH.next()) {
                if (rsListaTH.getString("vigencia").equals("t")) {
                    vigencia = "Vigente";
                } else {
                    vigencia = "No vigente";
                }

                modelo.addRow(new Object[]{
                    rsListaTH.getInt("codtipohabitacion"),
                    rsListaTH.getString("nombre"),
                    rsListaTH.getString("descripcion"),
                    rsListaTH.getDouble("costo"),
                    vigencia
                });

            }
            txtTotalTipo.setText(String.valueOf(objTH.totalTipoHab()));

        } catch (Exception e) {
        } finally{
        rsListaTH = null;
        }

    }
